fn draw_char(
    canvas: &mut sdl2::render::Canvas<sdl2::video::Window>,
    c: char,
    x: i32,
    y: i32,
    scale: i32,
    color: Color,
) {
    let bitmap = char_bitmap(c);
    canvas.set_draw_color(color);
    for row in 0..7 {
        let bits = bitmap[row as usize];
        for col in 0..5 {
            if bits & (1 << (4 - col)) != 0 {
                let _ = canvas.fill_rect(Rect::new(
                    x + col * scale,
                    y + row * scale,
                    scale as u32,
                    scale as u32,
                ));
            }
        }
    }
}

fn draw_text(
    canvas: &mut sdl2::render::Canvas<sdl2::video::Window>,
    text: &str,
    x: i32,
    y: i32,
    scale: i32,
    color: Color,
) {
    let mut cx = x;
    for ch in text.chars() {
        draw_char(canvas, ch, cx, y, scale, color);
        cx += 6 * scale;
    }
}

pub fn render_help_overlay(canvas: &mut sdl2::render::Canvas<sdl2::video::Window>) {
    let (win_w, win_h) = canvas.output_size().unwrap_or((960, 720));

    // Semi-transparent background
    canvas.set_blend_mode(sdl2::render::BlendMode::Blend);
    canvas.set_draw_color(Color::RGBA(0, 0, 0, 200));
    let _ = canvas.fill_rect(Rect::new(0, 0, win_w, win_h));

    let scale = 3i32;
    let line_h = 8 * scale;
    let margin = 50i32;
    let mut y = 30i32;

    // Title
    let title = "TurtleBox - Controls";
    let tw = title.len() as i32 * 6 * scale;
    draw_text(
        canvas,
        title,
        (win_w as i32 - tw) / 2,
        y,
        scale,
        Color::RGB(255, 255, 0),
    );
    y += line_h + 20;

    // Column positions
    let col1 = margin;
    let col2 = (win_w as i32) / 2 + 10;

    // Section headers
    draw_text(
        canvas,
        "Keyboard (P2)",
        col1,
        y,
        scale,
        Color::RGB(100, 200, 255),
    );
    draw_text(
        canvas,
        "Xbox Controller (P1)",
        col2,
        y,
        scale,
        Color::RGB(100, 200, 255),
    );
    y += line_h + 8;

    let gray = Color::RGB(180, 180, 180);
    let left = [
        "Arrow Keys - Move",
        "A - Jump (NES A)",
        "D - Attack (NES B)",
        "Enter - Start",
        "Right Shift - Select",
        "F1 - Toggle This Help",
        "ESC - Quit",
    ];
    let right = [
        "DPad/LStick - Move",
        "LB - Jump (NES A)",
        "RB - Attack (NES B)",
        "Start - Start",
        "Back - Select",
    ];

    for i in 0..left.len().max(right.len()) {
        if i < left.len() {
            draw_text(canvas, left[i], col1, y, scale, gray);
        }
        if i < right.len() {
            draw_text(canvas, right[i], col2, y, scale, gray);
        }
        y += line_h + 4;
    }

    // Footer
    y += 16;
    let footer = "Press F1 to close";
    let fw = footer.len() as i32 * 6 * scale;
    draw_text(
        canvas,
        footer,
        (win_w as i32 - fw) / 2,
        y,
        scale,
        Color::RGB(255, 255, 255),
    );
}
